
import java.util.Scanner;

public class _27WH_Pattern {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = 4;
        int line = 1 ;
        while ( line <= n){
          System.out.println("* * * *");
          line++;
        } 
        sc.close();
       }
}
