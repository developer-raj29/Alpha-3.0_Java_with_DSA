public class _52FunOver_Data {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static float sum(Float a, Float b) {
        return a + b;
    }

    public static void main(String[] args) {
        System.out.println(sum(3, 4));
        System.out.println(sum(3.0f, 4.8f));
    }
}
