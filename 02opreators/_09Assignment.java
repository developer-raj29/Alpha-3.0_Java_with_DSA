public class _09Assignment{
    public static void main(String[] args) {
        int a=10;
        
       System.out.println(a+=10);  // a=a+10=20
        System.out.println(a-=10);  // a=a-10=0
        System.out.println(a*=10);  // a=a*10=100
        System.out.println(a/=10);  // a=a/10=1
        System.out.println(a%=10);  // a=a%10=0

    }
}